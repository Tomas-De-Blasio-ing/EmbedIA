void model_predict(data3d_t input, data1d_t * output){
  
    prepare_buffers();
    
    //******************** LAYER 0 *******************//
    // Layer name: conv2d
    data3d_t output0;
    conv2d_layer(conv2d_data, input, &output0);
    // Debug function for layer conv2d
    print_data3d_t("conv2d", output0);
    
    //******************** LAYER 1 *******************//
    // Layer name: conv2d1
    
    // Debug function for layer conv2d1
    print_data3d_t("conv2d1", output0);
    
    //******************** LAYER 2 *******************//
    // Layer name: leaky_re_lu
    leakyrelu_activation(output0.data, 800, 39322);
    // Debug function for layer leaky_re_lu
    print_data3d_t("leaky_re_lu", output0);
    
    //******************** LAYER 3 *******************//
    // Layer name: max_pooling2d
    input = output0;
    static const pooling2d_layer_t max_pooling2d_data = { 2, 2 };
    max_pooling2d_layer(max_pooling2d_data, input, &output0);
    // Debug function for layer max_pooling2d
    print_data3d_t("max_pooling2d", output0);
    
    //******************** LAYER 4 *******************//
    // Layer name: conv2d_1
    input = output0;
    conv2d_layer(conv2d_1_data, input, &output0);
    // Debug function for layer conv2d_1
    print_data3d_t("conv2d_1", output0);
    
    //******************** LAYER 5 *******************//
    // Layer name: conv2d_11
    
    // Debug function for layer conv2d_11
    print_data3d_t("conv2d_11", output0);
    
    //******************** LAYER 6 *******************//
    // Layer name: leaky_re_lu_1
    leakyrelu_activation(output0.data, 144, 39322);
    // Debug function for layer leaky_re_lu_1
    print_data3d_t("leaky_re_lu_1", output0);
    
    //******************** LAYER 7 *******************//
    // Layer name: max_pooling2d_1
    input = output0;
    static const pooling2d_layer_t max_pooling2d_1_data = { 2, 2 };
    max_pooling2d_layer(max_pooling2d_1_data, input, &output0);
    // Debug function for layer max_pooling2d_1
    print_data3d_t("max_pooling2d_1", output0);
    
    //******************** LAYER 8 *******************//
    // Layer name: flatten
    input = output0;
    data1d_t output1;
    flatten3d_layer(input, &output1);
    // Debug function for layer flatten
    print_data1d_t("flatten", output1);
    
    //******************** LAYER 9 *******************//
    // Layer name: dense
    data1d_t input1;
    input1 = output1;
    dense_layer(dense_data, input1, &output1);
    
    // Debug function for layer dense
    print_data1d_t("dense", output1);
    
    //******************** LAYER 10 *******************//
    // Layer name: dense1
    
    // Debug function for layer dense1
    print_data1d_t("dense1", output1);
    
    //******************** LAYER 11 *******************//
    // Layer name: leaky_re_lu_2
    leakyrelu_activation(output1.data, 16, 39322);
    // Debug function for layer leaky_re_lu_2
    print_data1d_t("leaky_re_lu_2", output1);
    
    //******************** LAYER 12 *******************//
    // Layer name: dense_1
    input1 = output1;
    dense_layer(dense_1_data, input1, &output1);
    
    // Debug function for layer dense_1
    print_data1d_t("dense_1", output1);
    
    //******************** LAYER 13 *******************//
    // Layer name: dense_11
    
    // Debug function for layer dense_11
    print_data1d_t("dense_11", output1);
    
    //******************** LAYER 14 *******************//
    // Layer name: activation
    softmax_activation(output1.data, 10);
    // Debug function for layer activation
    print_data1d_t("activation", output1);
    

    *output = output1;

}

int model_predict_class(data3d_t input, data1d_t * results){
  
   
    model_predict(input, results);
    
    return argmax(*results);
    //return argmax(data1d_t);

}

