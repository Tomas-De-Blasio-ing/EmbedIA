void model_init(){
    conv2d_data = init_conv2d_data();
    conv2d_1_data = init_conv2d_1_data();
    dense_data = init_dense_data();
    dense_1_data = init_dense_1_data();

}

void model_predict(data3d_t input, data1d_t * output){
  
    prepare_buffers();
    
    //******************** LAYER 0 *******************//
    // Layer name: conv2d
    data3d_t output0;
    conv2d_layer(conv2d_data, input, &output0);
    
    //******************** LAYER 1 *******************//
    // Layer name: conv2d1
    relu_activation(output0.data, 288);
    
    //******************** LAYER 2 *******************//
    // Layer name: max_pooling2d
    input = output0;
    static const pooling2d_layer_t max_pooling2d_data = { 2, 2 };
    max_pooling2d_layer(max_pooling2d_data, input, &output0);
    
    //******************** LAYER 3 *******************//
    // Layer name: conv2d_1
    input = output0;
    conv2d_layer(conv2d_1_data, input, &output0);
    
    //******************** LAYER 4 *******************//
    // Layer name: conv2d_11
    relu_activation(output0.data, 64);
    
    //******************** LAYER 5 *******************//
    // Layer name: flatten
    input = output0;
    data1d_t output1;
    flatten3d_layer(input, &output1);
    
    //******************** LAYER 6 *******************//
    // Layer name: dense
    data1d_t input1;
    input1 = output1;
    dense_layer(&dense_data, &input1, &output1);
    
    
    //******************** LAYER 7 *******************//
    // Layer name: dense1
    relu_activation(output1.data, 16);
    
    //******************** LAYER 8 *******************//
    // Layer name: dense_1
    input1 = output1;
    dense_layer(&dense_1_data, &input1, &output1);
    
    
    //******************** LAYER 9 *******************//
    // Layer name: dense_11
    softmax_activation(output1.data, 10);
    

    *output = output1;

}

int model_predict_class(data3d_t input, data1d_t * results){
  
   
    model_predict(input, results);
    
    return argmax(*results);
    //return argmax(data1d_t);

}

