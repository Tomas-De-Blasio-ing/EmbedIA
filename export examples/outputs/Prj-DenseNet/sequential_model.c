// Global Variables
normalization_layer_t Standard_Scaler_data;
dense_layer_t dense_data;
dense_layer_t dense_1_data;
dense_layer_t dense_2_data;


void model_init(){
    Standard_Scaler_data = init_Standard_Scaler_data();
    dense_data = init_dense_data();
    dense_1_data = init_dense_1_data();
    dense_2_data = init_dense_2_data();

}

void model_predict(data1d_t input, data1d_t * output){
  
    prepare_buffers();
    
    //******************** LAYER 0 *******************//
    // Layer name: Standard_Scaler
    data1d_t output0;
    standard_norm_layer(Standard_Scaler_data, input, &output0);
    // Debug function for layer Standard_Scaler
    print_data1d_t_quant("Standard_Scaler", output0, output0.qparam);
    
    //******************** LAYER 1 *******************//
    // Layer name: dense
    input = output0;
    dense_layer(&dense_data, &input, &output0);
    
    // Debug function for layer dense
    print_data1d_t_quant("dense", output0, output0.qparam);
    
    //******************** LAYER 2 *******************//
    // Layer name: dense1
    relu_activation(output0.data, 16, &output0.qparam);
    // Debug function for layer dense1
    print_data1d_t_quant("dense1", output0, output0.qparam);
    
    //******************** LAYER 3 *******************//
    // Layer name: dropout
    
    //******************** LAYER 4 *******************//
    // Layer name: dense_1
    input = output0;
    dense_layer(&dense_1_data, &input, &output0);
    
    // Debug function for layer dense_1
    print_data1d_t_quant("dense_1", output0, output0.qparam);
    
    //******************** LAYER 5 *******************//
    // Layer name: dense_11
    
    // Debug function for layer dense_11
    print_data1d_t_quant("dense_11", output0, output0.qparam);
    
    //******************** LAYER 6 *******************//
    // Layer name: activation
    relu_activation(output0.data, 8, &output0.qparam);
    // Debug function for layer activation
    print_data1d_t_quant("activation", output0, output0.qparam);
    
    //******************** LAYER 7 *******************//
    // Layer name: dropout_1
    
    //******************** LAYER 8 *******************//
    // Layer name: dense_2
    input = output0;
    dense_layer(&dense_2_data, &input, &output0);
    
    // Debug function for layer dense_2
    print_data1d_t_quant("dense_2", output0, output0.qparam);
    
    //******************** LAYER 9 *******************//
    // Layer name: dense_21
    sigmoid_activation(output0.data, 1, &output0.qparam);
    // Debug function for layer dense_21
    print_data1d_t_quant("dense_21", output0, output0.qparam);
    

    *output = output0;

}

int model_predict_class(data1d_t input, data1d_t * results){
  
   
    model_predict(input, results);
    
    return argmax(*results);
    //return argmax(data1d_t);

}

